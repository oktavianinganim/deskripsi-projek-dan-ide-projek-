<h3>
    DESKRIPSI PROJECT</h3>
<h5>Nama Proyek: Kalkulator Sederhana</h5>
<h5>Deskripsi Singkat: Aplikasi kalkulator sederhana yang memungkinkan pengguna melakukan operasi matematika dasar dan menyimpan hasil perhitungan ke dalam database.
Fitur yang Diharapkan:</h5>
1.Tampilan antarmuka yang intuitif: Desain antarmuka yang user-friendly dengan tombol dan field input yang jelas.
<h5>2.Operasi Matematika Dasar: Memungkinkan pengguna untuk melakukan penjumlahan, pengurangan, perkalian, dan pembagian.</h5>
<h5>3.Responsif: Aplikasi dapat diakses dan berfungsi dengan baik pada berbagai perangkat seperti desktop, tablet, dan smartphone.</h5>
</h5>4.Keamanan: Melakukan validasi input pengguna untuk memastikan bahwa hanya angka dan operator yang valid yang dapat dimasukkan.</h5>

<h4>Aplikasi kalkulator sederhana ini dirancang untuk membantu pengguna melakukan operasi matematika dasar seperti penjumlahan, pengurangan, perkalian, dan pembagian. Pengguna akan memasukkan angka dan operator melalui antarmuka yang intuitif.</h4> <h5>Setelah pengguna mengklik tombol "Hitung", hasil perhitungan akan ditampilkan secara langsung.
</h5>
<h5>Antarmuka aplikasi ini responsif, sehingga dapat diakses melalui berbagai perangkat seperti desktop, tablet, dan smartphone. Validasi input juga dilakukan untuk memastikan bahwa pengguna hanya memasukkan angka dan operator yang valid.

<h5>Dalam proyek ini, kita tidak menggunakan database untuk menyimpan riwayat perhitungan.</h5> Aplikasi ini hanya fokus pada fitur perhitungan matematika dasar dan menampilkan hasil secara langsung kepada pengguna.</h5>





